    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        int fila = tabla.getSelectedRow();
        
        if(fila!=-1){
            
            String[] datosAula = new String[3];
            datosAula=recuperarFila(fila);
         
            
            if(datosAula[2].equals("Multimedios")){
                ModificarMultimedios mMultimedios = new ModificarMultimedios(mBedel,datosAula[0]);
                mMultimedios.setVisible(true);
                mMultimedios.setLocationRelativeTo(null);
                mMultimedios.setResizable(false);
                this.setVisible(false);
                
            }else if(datosAula[2].equals("Informatica")){
                ModificarInformatica mInformatica = new ModificarInformatica(mBedel,datosAula[0]);
                mInformatica.setVisible(true);
                mInformatica.setLocationRelativeTo(null);
                mInformatica.setResizable(false);
                this.setVisible(false);
                
            }else{
                ModificarSinRecursos mSinRecursos = new ModificarSinRecursos(mBedel,datosAula[0]);
                mSinRecursos.setVisible(true);
                mSinRecursos.setLocationRelativeTo(null);
                mSinRecursos.setResizable(false);
                this.setVisible(false);
            }
            
            
        }else{
            mensajeNoSelecciona();
        }

    }//GEN-LAST:event_modificarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        
        String num = (String)numero.getSelectedItem();
        String tipo1 = (String)tipo.getSelectedItem();
        int capacidad = (int)capacidad1.getValue();
        
        ArrayList<AulaDTO> aulas = new ArrayList<>();
        aulas = gAula.buscarAulas(num,tipo1,capacidad);
        
        if(aulas.isEmpty()){
            mensajeFracaso();
        }else{
            cargarDatos(aulas);
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void centrarColumnas() {
        // Crear un renderizador para centrar el texto
        javax.swing.table.DefaultTableCellRenderer renderer = new javax.swing.table.DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

        // Aplicar el renderizador a cada columna de la tabla
        for (int i = 0; i < tabla.getColumnCount(); i++) {
            tabla.getColumnModel().getColumn(i).setCellRenderer(renderer);
        }
    }
    
    private void cargarDatos(ArrayList<AulaDTO> aulas) {
        // Obtén el modelo de la tabla
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tabla.getModel();

        // Limpia las filas existentes en la tabla
        model.setRowCount(0);

        // Agrega cada BedelDTO como una fila en la tabla
        for (AulaDTO aula : aulas) {
            model.addRow(new Object[]{
                aula.getNumero(), 
                aula.getPiso(),
                aula.getTipo(), 
                aula.getCapacidad(),
                aula.getDisponible()
            });
        }
    }
    
