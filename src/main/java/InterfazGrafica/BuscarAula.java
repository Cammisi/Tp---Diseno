    public String[] recuperarFila(int fila){
        int columnas = tabla.getColumnCount();
        String[] datosFila = new String[3];
        // Iterar por todas las columnas de la fila seleccionada
        for (int i = 0; i < 3; i++) {
            if(i==2){
                datosFila[i] = (String) tabla.getValueAt(fila, i);
            }else{
                datosFila[i] = String.valueOf(tabla.getValueAt(fila, i));
            } 
        }
        return datosFila;
    }
    
